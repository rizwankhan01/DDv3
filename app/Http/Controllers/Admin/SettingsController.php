     */
    public function update(Request $request, $id)
    {
        $user = user::findOrFail($id);
        $cp = $request->input('cur_password');
        $np = Hash::make($request->input('new_password'));
        if(Hash::check($cp,$user->password)){
          $user->password = $np;
          $user->update();
          return redirect('/settings')->with('status','Password Updated Successfully!');
        }else{
          return redirect('/settings')->with('status','Incorrect Current Password.');
        }
    }

